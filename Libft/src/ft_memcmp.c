#include "libft.h"

int		ft_memcmp(const void *p1, const void *p2, size_t n)
{
	size_t			i;
	char			*s1;
	char			*s2;

	s1 = (char *)p1;
	s2 = (char *)p2;
	i = 0;
	while (i < n)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
